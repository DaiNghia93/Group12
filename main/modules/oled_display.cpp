static void fb_clear(void) { memset(fb, 0, sizeof(fb)); }

static void fb_pixel(int x, int y)
{
    if (x < 0 || x >= 128 || y < 0 || y >= 64) return;
    fb[(y / 8) * 128 + x] |= (1 << (y % 8));
}

static void fb_char(int x, int y, char c)
{
    if (c < 32 || c > 127) return;
    const uint8_t *g = font5x7[(uint8_t)c - 32];
    for (int col = 0; col < 5; col++) {
        uint8_t line = g[col];
        for (int row = 0; row < 8; row++) {
            if (line & (1 << row))
                fb_pixel(x + col, y + row);
        }
    }
}

static void fb_str(int x, int y, const char *s)
{
    while (*s) { fb_char(x, y, *s++); x += 6; }
}

// Vẽ đường ngang (pixel đơn)
static void fb_hline(int x0, int x1, int y)
{
    for (int x = x0; x <= x1; x++) fb_pixel(x, y);
}

// Vẽ hình chữ nhật viền
static void fb_rect(int x, int y, int w, int h)
{
    fb_hline(x, x + w - 1, y);
    fb_hline(x, x + w - 1, y + h - 1);
    for (int i = y; i < y + h; i++) { fb_pixel(x, i); fb_pixel(x + w - 1, i); }
}

// Vẽ hình chữ nhật đặc (fill)
static void fb_fill_rect(int x, int y, int w, int h)
{
    for (int j = y; j < y + h; j++)
        for (int i = x; i < x + w; i++)
            fb_pixel(i, j);
}


static void draw_screen(void)
{
    // ---------- Thu thập dữ liệu ----------
    bool  wifi_ok   = wifi_is_connected();
    bool  manual    = wifi_is_manual_mode();
    int   speed_lvl = wifi_get_speed_level();
    bool  fan_on    = wifi_get_fan_state();
    float temp      = fan_get_temperature();
    CeAiData ai     = ce_parser_get_data();

    // ---------- Xóa buffer ----------
    fb_clear();

    // ===== HEADER (y=0..9) =====

    fb_str(17, 1, "**  SMART FAN  **");
    // Đường kẻ dưới header
    fb_hline(0, 127, 10);

    // ===== WIFI (y=12) =====
    if (wifi_ok) {
        fb_str(0, 12, "WiFi: CONNECTED");
    } else {
        fb_str(0, 12, "WiFi: NO SIGNAL");
    }

    // ===== NHIỆT ĐỘ (y=22) =====
    char line_temp[32];
    snprintf(line_temp, sizeof(line_temp), "Temp: %.1f C", temp);
    fb_str(0, 22, line_temp);

    // ===== NGƯỜI (y=32) =====
    if (ai.person_count > 0) {
        char pbuf[24];
        snprintf(pbuf, sizeof(pbuf), "Person: YES (%d)", ai.person_count);
        fb_str(0, 32, pbuf);
    } else {
        fb_str(0, 32, "Person: NONE");
    }

    // ===== MODE (y=42) =====
    char mode_buf[24];
    snprintf(mode_buf, sizeof(mode_buf), "Mode: %s", manual ? "MANUAL" : "AUTO");
    fb_str(0, 42, mode_buf);
}

