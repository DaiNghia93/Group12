static void oled_cmd(uint8_t cmd)
{
    i2c_cmd_handle_t h = i2c_cmd_link_create();
    i2c_master_start(h);
    i2c_master_write_byte(h, (OLED_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(h, 0x00, true); // Co=0, D/C=0 -> command
    i2c_master_write_byte(h, cmd,  true);
    i2c_master_stop(h);
    i2c_master_cmd_begin(OLED_I2C_PORT, h, pdMS_TO_TICKS(50));
    i2c_cmd_link_delete(h);
}

static void oled_hw_init(void)
{
    vTaskDelay(pdMS_TO_TICKS(100));
    oled_cmd(0xAE);             // OFF
    oled_cmd(0xD5); oled_cmd(0x80);
    oled_cmd(0xA8); oled_cmd(0x3F); // mux 64
    oled_cmd(0xD3); oled_cmd(0x00);
    oled_cmd(0x40);
    oled_cmd(0x8D); oled_cmd(0x14); // charge pump
    oled_cmd(0x20); oled_cmd(0x00); // horizontal addressing
    oled_cmd(0xA1);                  // seg remap (flip ngang nếu cần → 0xA0)
    oled_cmd(0xC8);                  // com scan dir  (flip dọc  nếu cần → 0xC0)
    oled_cmd(0xDA); oled_cmd(0x12);
    oled_cmd(0x81); oled_cmd(0xCF); // contrast
    oled_cmd(0xD9); oled_cmd(0xF1);
    oled_cmd(0xDB); oled_cmd(0x40);
    oled_cmd(0xA4);
    oled_cmd(0xA6);                  // normal (không đảo màu)
    oled_cmd(0xAF);                  // ON
}

static void oled_flush(void)
{
    oled_cmd(0x21); oled_cmd(0x00); oled_cmd(0x7F); // col 0..127
    oled_cmd(0x22); oled_cmd(0x00); oled_cmd(0x07); // page 0..7

    i2c_cmd_handle_t h = i2c_cmd_link_create();
    i2c_master_start(h);
    i2c_master_write_byte(h, (OLED_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(h, 0x40, true); // D/C=1 -> data
    i2c_master_write(h, fb, 1024, true);
    i2c_master_stop(h);
    i2c_master_cmd_begin(OLED_I2C_PORT, h, pdMS_TO_TICKS(200));
    i2c_cmd_link_delete(h);
}

