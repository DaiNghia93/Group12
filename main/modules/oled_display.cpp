void oled_display_init(void)
{
    // Cấu hình I2C_NUM_1
    i2c_config_t conf = {};
    conf.mode           = I2C_MODE_MASTER;
    conf.sda_io_num     = OLED_SDA_GPIO;
    conf.scl_io_num     = OLED_SCL_GPIO;
    conf.sda_pullup_en  = GPIO_PULLUP_ENABLE;
    conf.scl_pullup_en  = GPIO_PULLUP_ENABLE;
    conf.master.clk_speed = OLED_I2C_HZ;

    ESP_ERROR_CHECK(i2c_param_config(OLED_I2C_PORT, &conf));
    ESP_ERROR_CHECK(i2c_driver_install(OLED_I2C_PORT, conf.mode, 0, 0, 0));

    oled_hw_init();

    ESP_LOGI(TAG, "OLED SSD1306 da khoi tao (I2C1, SDA=%d, SCL=%d)",
             OLED_SDA_GPIO, OLED_SCL_GPIO);
}

void oled_display_task(void *pv)
{
    ESP_LOGI(TAG, "OLED task chay.");

    while (1) {
        draw_screen();
        oled_flush();
        vTaskDelay(pdMS_TO_TICKS(500)); // refresh 2 lần/giây
    }
}